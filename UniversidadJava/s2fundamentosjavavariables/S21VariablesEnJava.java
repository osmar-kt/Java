package s2fundamentosjavavariables;

public class S21VariablesEnJava {

    public static void main(String[] args) {
        //Definimos variable tipo primitiva
        int miVariableEntera = 10;//notacion de camello
        System.out.println(miVariableEntera);

        //Modificamoes el valor de la variable
        miVariableEntera = 5;
        System.out.println(miVariableEntera);

        //Definimos String
        String miVariableCadena = "Saludos";
        System.out.println(miVariableCadena);

        //Modificamoes el valor
        miVariableCadena = "Adios";
        System.out.println(miVariableCadena);
    }
}

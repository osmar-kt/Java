package s2fundamentosjavavariables;

public class S22InferenciaDeTiposEnJava {

    public static void main(String[] args) {
        //Tenemos 2 Literales: Numericas y textuales
        //Var hace inferencia de datos a partir del valor asigando
        var miVariableEntera = 2;//es de tipo entero
        System.out.println(miVariableEntera);
        var miVariableCadena = "Hola";//String
        System.out.println(miVariableCadena);
    }
}
